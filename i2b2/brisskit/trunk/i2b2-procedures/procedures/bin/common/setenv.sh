#!/bin/bash
#
# Basic environment variables for i2b2
# 
# Invocation within another sh script should be:
# source $I2B2_PROCEDURES_HOME/setenv.sh
#
#-------------------------------------------------------------------
source $I2B2_PROCEDURES_HOME/config/defaults.sh

if [ -z $I2B2_DEFAULTS_DEFINED ]
then
	I2B2_DEFAULTS_DEFINED=true
	
	export I2B2_DEFAULTS_DEFINED
	export JOB_LOG_NAME ONYX_EXPORT_DIRECTORY METADATA_DIRECTORY 
	export REFINED_METADATA_DIRECTORY METADATA_SQL_DIRECTORY PDO_DIRECTORY
	export PDO_SQL_DIRECTORY BATCH_SIZE MAIN_REFINED_METADATA_FILE_NAME
	export DB_TYPE ZIPPED_SQL_DIRECTORY
	export DOMAIN_ID DOMAIN_NAME DOMAIN_HELP_URL
	export REFINED_METADATA_ENUMS_DIRECTORY
	export CRC_DS ONT_DS WORK_DS
	
	export REFINED_METADATA_CATISSUE_DIRECTORY
	export REFINED_METADATA_CATISSUE_ENUMS_DIRECTORY
	export METADATA_CATISSUE_SQL_DIRECTORY
	export MAIN_REFINED_METADATA_CATISSUE_FILE_NAME
	
	export JBOSS_HOME
	
	if [ ! -z $I2B2_PROCEDURES_WORKSPACE ]
	then
		export I2B2_PROCEDURES_WORKSPACE 
	fi
	
fi


